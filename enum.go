package main

import (
	"os"
	"strings"
	"text/template"

	"golang.org/x/text/cases"
	"golang.org/x/text/language"
)

func Run(fileName string, packageName string, typeName string, noPrefix bool, values ...string) error {
	data := make(map[string]interface{})
	data["Package"] = packageName
	data["Items"] = values
	data["Type"] = typeName
	data["Prefix"] = ""
	if !noPrefix {
		data["Prefix"] = typeName
	}
	f, err := os.Create(fileName)
	if err != nil {
		return err
	}
	defer f.Close()

	funcMap := template.FuncMap{
		"ToLower": strings.ToLower,
		"Title":   Title,
	}

	t2 := template.New("t_enum").Funcs(funcMap)
	t2, err = t2.Parse(fileTemplate)
	if err != nil {
		return err
	}
	return t2.Execute(f, data)
}

func Title(s string) string {
	return cases.Title(language.English).String(s)
}

